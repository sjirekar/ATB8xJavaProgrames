package Task;

public class Task_27092024_Task_2 {
    public static void main(String[] args) {
        //Calculate the Sum of numbers 1 to 100;
       //Working with while loop
        int sum =0;
        int i =1;
        while (i<100)
        {
            sum = sum +1;
            i++;
        }
        System.out.println(sum);
    }
}
