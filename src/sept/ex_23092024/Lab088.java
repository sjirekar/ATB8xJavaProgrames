package sept.ex_23092024;

public class Lab088 {
    public static void main(String[] args) {
        //char c = 'a' -> vowel or consonant
        //vowel -> a, e, i, o, u
        //rest are consonant
        char ch = 'a';
        if (ch == 'a' || ch == 'e' || ch == 'i'|| ch == 'o'|| ch == 'u')
        {
            System.out.println("Vowel");
        }
        else {
            System.out.println("Consonant");
        }
    }

}
