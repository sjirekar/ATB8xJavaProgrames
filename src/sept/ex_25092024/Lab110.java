package sept.ex_25092024;

public class Lab110 {
    public static void main(String[] args) {
        //while
        //While - A, B, C
        //A -> Initialization
        //B -> Condition
        //C -> Increment or decrement
        //In while case -> Initialization is outside
        int i=0; //A
        while(i<=10) //B
        {
            System.out.println(i);
            i++; //C
        }
    }
}
